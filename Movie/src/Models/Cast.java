package Models;

public class Cast {
    private String castFullName;
    private String role;

    public String getCastFullName() {
        return castFullName;
    }

    public void setCastFullName(String castFullName) {
        this.castFullName = castFullName;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }
}
